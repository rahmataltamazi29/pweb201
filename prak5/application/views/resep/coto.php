      <h2>4.Coto Makassar</h2>
      <img src="asset\coto makassar.jpg"alt="Coto makassar" width="400" height="250">
      <p>Ketika menyebutkan jenis-jenis masakan terkenal asal Sulawesi Selatan pastinya tidak
      akan jauh dari yang namanya Coto Makassar. Maklum, kuliner kota ini memang terkenal
      akan beragam masakan daging serta kuahnya. Itulah serunya berkunjung ke kota tepi laut
      ini karena ada beberapa masakan daging berkuah yang sejatinya berbeda seperti
      Pallubasa, Konro, hingga Sop Saudara.</p>

      <h4>Bahan-bahan:</h4>
      <ol>
      <li>500g daging sapi has dalam, potong dadu</li>
      <li>2 batang serai</li>
      <li>1 cm lengkuas</li>
      <li>1 cm jahe</li>
      <li>3 lembar daun salam</li>
      <li>200g kacang tanah goreng tanpa kulit, haluskan</li>
      <li>2L air</li>
      <li>1.5 sdm Royco Kaldu Sapi</li>
      <li>2 sdm Bango Kecap Manis</li>
      <li>3 sdm minyak</li>
      <li>10 buah cabai merah keriting</li>
      <li>100g tauco</li>
      <li>8 siung bawang putih</li>
      <li>6 butir kemiri, sangrai</li>
      <li>1 sdm ketumbar, sangrai</li>
      <li>1 sdm gula merah</li>
      </ol>
      <h4>Cara membuat:</h4>
      <ol>
      <li>Panaskan minyak, tumis bumbu halus, serai, lengkuas, jahe,
      dan daun salam hingga harum. Masukkan daging sapi, aduk hingga berubah warna.</li>
      <li>Tuang air, Royco Kaldu Sapi, masak hingga mendidih.</li>
      <li>Tambahkan Bango Kecap Manis dan kacang tanah goreng, aduk rata. Angkat.</li>
      <li>Sajikan dengan pelengkap. Dapat ditambahkan dengan bawang goreng,
      irisan daun bawang, dan seledri. Sajikan dengan buras/lontong.</li>
      </ol>
