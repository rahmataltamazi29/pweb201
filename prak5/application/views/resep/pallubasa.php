<h4>Cara Membuat:</h4>
<ol>
<li>Rebus daging sapi hingga empuk. Angkat dan dinginkan, lalu potong-potong kotak. Sisihkan 600 ml air sisa
  rebusan daging sebagai kaldu sapi.</li>
<li>Panaskan minyak. Tumis bumbu halus bersama dengan gula merah, serai, lengkuas, kayu manis, cengkeh
  hingga harum dan matang.</li>
<li>Tuangkan air kaldu sapi. Aduk rata lalu masak dengan api kecil hingga mendidih.</li>
<li>Masukkan air asam, garam, penyedap rasa, dan kelapa sangrai. Aduk rata dan masak hingga bumbu meresap
  dan kuah mengental. Angkat Siap disajikan..</li>
</ol>



