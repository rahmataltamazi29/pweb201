               <h2>2.Balado Ayam Pandan</h2>
                 <img src="asset\Balado.jpg" width="400">
                  <p>Penyuka pedas pasti takkan melewatkan resep balado ayam pandan yang bukan cuma pedas
                  tetapi juga aromatik ini. Ide balado ayam pandan yang dimasak dengan bumbu lain yang
                  membuatnya menarik ini mirip seperti masakan Sulawesi Utara. Ia memadukan serai, daun
                  pandan, dan daun jeruk ke dalam masakannya.</p>

        <h4>Bahan-bahan:</h4>
        <ol>
             <li>1ekor ayam, potong 16 bagian</li>
             <li>2batang serai, memarkan</li>
             <li>6butir bawang merah, iris</li>
             <li>1lembar daun pandan</li>
             <li>5 lembar daun jeruk</li>
             <li>5 sdm minyak goreng</li>
             <li>150cc air</li>
             <li>1sdm Royco Kaldu Ayam</li>
             <li>1buah jeruk nipis, ambil airnya</li>
             <li>12buah cabai merah</li>
             <li>10buah cabai rawit merah</li>
             <li>7siung bawang putih</li>
             <li>4cm jahe</li>
        </ol>
        <h4>Cara membuat:</h4>
        <ol>
            <li>Haluskan cabai merah, cabai rawit merah, bawang putih dan jahe, sisihkan.</li>
            <li>Tumis bawang merah, serai dan daun pandan hingga harum dan kekuningan.
            Masukkan bumbu yang dihaluskan dan daun jeruk, Royco Kaldu Ayam, dan aduk hingga harum.</li>
            <li>Kemudian masukkan ayam. Aduk hingga ayam berubah warnanya. Tambahkan air, kecilkan api,dan tutup.</li>
            <li>Ungkep hingga airnya agak mengering sambil sekali-sekali diaduk-aduk. Angkat.</li>
            <li>Tambahkan air jeruk, aduk hingga rata. Setelah matang, sajikan.</li>
        </ol>


