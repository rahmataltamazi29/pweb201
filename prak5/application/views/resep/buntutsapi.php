        <h2>3.Buntut Sapi Khas Minang</h2>
         <img src="asset\Buntut Sapi Kuah Asam Padeh Khas Minang.jpg" width="400">
            <p>Terkenal akan kesegarannya, menu ini memiliki banyak penggemar di Indonesia.
            Pada umumnya, boga bahari seperti ikan yang menjadi bahan utamanya. Kali ini,
            kita akan menggunakan buntut sapi yang bisa memberikan rasa yang otentik.
            Kandungan gelatin alias protein kolagen pada buntut akan keluar setelah diproses dalam waktu lama.
            Kandungan ini yang nantinya membuat buntut empuk dan menimbulkan rasa gurih. Lalu rasa ini bertemu
            dengan bumbu cabai yang cukup pedas serta asam dari belimbing sayur. Istimewa bukan menu ini,
            yuk segera praktikkan resep ini di rumah.</p>

  <h4>Bahan-bahan:</h4>
  <ol>
       <li>1kg buntut sapi, rebus hingga setengah matang, tiriskan</li>
       <li>1batang serai, memarkan</li>
       <li>3cm lengkuas, memarkan</li>
       <li>3buah asam kandis</li>
       <li>5lembar daun jeruk, buang tulang dan iris memanjang</li>
       <li>3lembar daun salam</li>
       <li>5buah belimbing sayur, belah 2</li>
       <li>1lembar daun kunyit</li>
       <li>2L air mendidih</li>
       <li>1sdt Royco Kaldu Sapi</li>
       <li>2sdm Bango Kecap Manis</li>
       <li>2sdm minyak, untuk menumis</li>
       <li>200g cabai merah keriting</li>
       <li>15butir bawang merah</li>
       <li>10siung bawang putih</li>
  </ol>

  <h4>Cara membuat:</h4>
  <ol>
      <li>Panaskan minyak, tumis bumbu halus hingga harum. Angkat dan sisihkan.</li>
      <li>Panaskan air dalam panci, masukkan buntut dan semua bahan kecuali belimbing sayur. Aduk rata.</li>
      <li>Masukkan tumisan bumbu, Royco Kaldu Sapi dan Bango Kecp Manis, aduk rata. Masak hingga kuah mengental dan buntut empuk.</li>
      <li>Masukkan potongan belimbing sayur, aduk rata. Angkat dan sajikan.</li>
  </ol>
