        <h2>5.Gado-Gado Surabaya</h2>
          <img src="asset\Gado-Gado Surabaya.jpg" alt="Gado-Gado Surabaya"
           width="400" height="250">
            <p>resep gado-gado Surabaya yang memiliki keunikan tersendiri dibandingkan lainnya.
            Menyenangkannya juga, kalau bicara soal hidangan ini, pastinya tidak jauh dari rasa
            lezat dan juga proses memasaknya yang mudah.
            Perbedaan pertama yang menarik dari gado-gado Surabaya adalah keberadaan wortel dan
            tomat. Meskipun pilihan sayur mayur lainnya relatif mirip, keberadaan dua elemen ini
            memberikan rasa tersendiri yang unik. Selanjutnya, versi Surabaya memiliki
            karakteristik sambal kacang yang lebih encer dibandingkan saudara-saudaranya
            yang datang dari Jawa bagian barat.</p>

<h4>Bahan-bahan:</h4>
<ol>
       <li>2L air</li>
       <li>250g kol, siangi</li>
       <li>100g wortel, iris</li>
       <li>100g kacang panjang, potong 4 cm</li>
       <li>2 sdm minyak, untuk menumis</li>
       <li>2 sdt Royco Kaldu Ayam</li>
       <li>4 lembar daun jeruk, sobek</li>
       <li>250g kentang, kukus dan potong dadu</li>
       <li>200g mentimun, potong</li>
       <li>150g selada keriting, sobek</li>
       <li>2 buah tomat, belah enam memanjang</li>
       <li>2 buah lontong</li>
       <li>4 butir telur ayam, rebus dan belah dua</li>
       <li>4 buah tahu putih, goreng dan potong empat bagian</li>
       <li>200g tempe, potong dadu dan goreng</li>
       <li>200ml air</li>
       <li>200ml santan cair</li>
       <li>250g kacang tanah, kupas dan sangrai</li>
       <li>4 siung bawang putih</li>
       <li>3 buah cabai rawit merah</li>
       <li>3 sdm air asam jawa</li>
       <li>2 sdm gula merah</li>
</ol>
<h4>Cara membuat:</h4>
<ol>
       <li>Didihkan air, rebus kembang kol, wortel, dan kacang panjang secara bergantian. Sisihkan.</li>
       <li>Membuat sambal kacang: Masukkan semua bumbu halus ke dalam blender. Proses hingga halus. Sisihkan.</li>
       <li>Panaskan minyak, masukkan bahan sambal kacang yang sudah dihaluskan, Royco Kaldu Ayam dan daun jeruk, aduk.
       Masak hingga matang.</li>
       <li>tata semua sayuran, lontong, telur, tempe dan tahu di atas piring saji. Siram dengan sambal kacang. Sajikan.</li>
</ol>

